<h4>PRESENTACIONS</h4>
<p>Quant a les presentacions, la idea és que les empreses intentin ser originals de cara a destacar una mica però sempre i només per transmetre millor els continguts de la seva presentació.</p>
<p>- Les presentacions es gravaran.</p>
<h5>Coses que no es poden fer a l'escenari</h5>
<p>- L’objectiu de les presentacions és superar la por escènica i comunicar-se en viu. Per tant, no es pot presentar un vídeo durant 2 minuts sense que els estudiants participin de manera activa a la presentació.</p>
<p>- No es pot fer volar res per l'escenari ni per sobre del públic (drones o altres aparells).</p>
<p>- No es pot utilitzar res que pugui generar espurnes, com a material pirotècnic o altres coses menys òbvies com els típics "canons de confeti".</p>
<p>- No es pot utilitzar res que embruti l'escenari (repetim el cas del canó de confeti).</p>
<p>- No podem fer ni preparar res "especial" per cada empresa, com apagar els llums de la sala, encendre del tot o tenir material extra com taules o cadires. Cada empresa ha de disposar del seu material per fer la presentació.</p>

<h5>Bases de les presentacions</h5>
<p><b>És obligatori  que tots els membres de l’empresa participin en les presentacions.</b></p>
<p>Les presentacions han de tenir una durada màxima de 2 minuts (incloent qualsevol cosa que es vulgui preparar a l'escenari).</p>
<p>Es valorarà positivament que la presentació contingui alguna part en anglès (tant la part oral com les diapositives).</p>
<p>És obligatori que totes les empreses estiguin a l’esglèsia durant el torn de presentacions.</p>
<p>Els criteris amb que es valoraran les presentacions seran els següents: (el jurat posarà una puntuació per cada concepte)</p>
<p>1.	Disseny general de la presentació: Estètica de la presentació del conjunt. Imatge general de l’empresa. El vídeo ha estat un recurs original i personalitzat de l’empresa.</p>
<p>2.	Organització, estructura i presentació del contingut: Queda clar el producte o servei que han presentat? El vídeo ha ajudat a transmetre clarament l’activitat de l’empresa. S’ha respectat el temps de presentació. Han participat tots els membres de l’empresa?</p>
<p>3.	Llenguatge verbal i no verbal. Es tindrà en compte la confiança i la facilitat d’establir contacte visual amb el públic i de fer-los partícips de la presentació. Ser assertiu a l’hora de fer la presentació, per tal de no avorrir ni donar informació innecessària. S’ha d’utilitzar un vocabulari clar i fluid. El discurs de tots els membres ha de seguir una estructura clara i ordenada. És important transmetre la informació de manera àgil, sense llegir i usant una gesticulació adequada. Cal saber estar dalt de l’escenari.</p>
<p>4.	Originalitat La idea és que les empreses intentin ser originals de cara a destacar una mica però sempre i només per transmetre millor els continguts de la seva presentació.</p>
<p>Quina empresa t’ha agradat més? Quina empresa ha sabut lligar millor el video i la posada en escena?</p>
<p>- Es penalitzarà si es sobrepassa el límit de temps.</p>
<p>- Com a apunt final, val a dir que no es tindrà en compte el tipus d'empresa ni la   viabilitat, sinó com s'exposen els continguts.</p>

TAULA

<p>Les presentacions s’hauran de penjar a Youtube i després caldrà pujar l’enllaç reproduïble a la pàgina web de firaempresa.</p>
<p>No s’acceptarà l’entrega de pendrives amb canvis d’última hora durant l’esdeveniment.</p>
<p>A l'escenari à disposareu també de 2 micròfons de mà i un de fixe.</p>

